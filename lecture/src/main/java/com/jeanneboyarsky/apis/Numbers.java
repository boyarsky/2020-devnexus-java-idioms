package com.jeanneboyarsky.apis;

public class Numbers {

    public int absoluteValue(int x) {
        return Math.abs(x);
    }

    public int biggest(int x, int y) {
        return Math.max(x, y);
    }

    public int smallest(int x, int y) {
        return Math.min(x, y);
    }

    public double power(double x, double y) {
        return Math.pow(x, y);
    }
}
